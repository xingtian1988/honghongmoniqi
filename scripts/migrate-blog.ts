async function migrateArticles() {
  const supabase = getSupabaseClient();

  console.log("开始迁移文章到数据库...");

  for (const article of articles) {
    const { data, error } = await supabase
      .from("blog_posts")
      .insert(article)
      .select("id, title")
      .single();

    if (error) {
      console.error(`插入失败: ${article.title}`, error);
    } else {
      console.log(`插入成功: ${data.title} (ID: ${data.id})`);
    }
  }

  console.log("迁移完成！");
}

// 执行迁移
migrateArticles()
  .then(() => process.exit(0))
  .catch((err) => {
    console.error("迁移失败:", err);
    process.exit(1);
  });
